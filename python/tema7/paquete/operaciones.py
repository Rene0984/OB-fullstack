def suma(a1, b2):
    """suma a1 mas b2"""
    return a1 + b2


def resta(a1, b2):
    """resta a1 menos b2"""
    return a1 - b2


def multiplicacion(a1, b2):
    """multiplica a1 por b2"""
    return a1 * b2


def division(a1, b2):
    """divide a1 entre b2"""
    return a1 / b2
