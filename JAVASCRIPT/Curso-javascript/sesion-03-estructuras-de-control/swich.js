let nota = 1

switch (nota) {
    case 5:
        console.log("buen trabajo sobresaliente");
        break
    case 4:
        console.log("buen trabajo pero puedes mejorar");
        break
    case 3:
        console.log("has obtenido un suficiente");
        break
    case 2:
        console.log("no has aprobado por poco");
        break
    case 1:
        console.log("no has estudiado nada, trabaja mas para la proxima");
        break
    default:
        console.log("Error");
        break
}